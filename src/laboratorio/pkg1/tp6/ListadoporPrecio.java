private void buscar (){
      borrarFilas();
        if (!jTp2.getText().equals("")&&!jTp1.getText().equals("")) {
   

         int Mayor = Integer.parseInt(jTp2.getText());
        int Menor = Integer.parseInt(jTp1.getText());
        for (Producto producto : SuperMercado.productos) {

            if (producto.getPrecio() >= Menor && producto.getPrecio() <= Mayor ) {
                modelo.addRow(new Object[]{producto.getCodigo(), producto.getDescripcion(),  producto.getPrecio(),producto.getStock(),});
                   
                  
            }     
            }
        }
} 

